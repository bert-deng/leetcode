class Solution {
    public int countSubstrings(String s) {
        int ans = 0;
        int len = s.length() - 1;
        boolean [][] dp = new boolean[len][len];
        for(int i = len - 1;i>=0;i--)
        {
            for(int j = 0;j<len;j++)
            {
                if(s.charAt(i) != s.charAt(j) || j > i) continue;
                if(i -j <=1) {
                    ans++;
                    dp[i][j] = true;
                }

                if(dp[i + 1][j - 1])
                {
                    ans++;
                    dp[i][j] = true;
                }

            }
        }
        return ans;

    }
}
