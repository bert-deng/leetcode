class Solution {
    public int findLHS(int[] nums) {
       Arrays.sort(nums);
       int begin = 0;
       int end = 0;
       int ans = 0;
       for(;end < nums.length;end++)
       {
           while(nums[end] - nums[begin] > 1)
               begin++;
           if(nums[end] - nums[begin] == 1)
               ans = Math.max(ans, end - begin + 1);
       }
       return ans;
    }
}
